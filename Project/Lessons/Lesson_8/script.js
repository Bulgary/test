let num = 24;

switch (num) {
    case num < 49:
        console.log("Wrong");
        break;
    case num > 100:
        console.log("Too much");
        break;
    case num > 80:
        console.log("Still a lot");
        break;
    case 50:
        console.log("Yep");
        break;
    default:
        console.log("Something went wrong!");
        break;
}